            "𝓶","𝓷","𝓸","𝓹","𝓺","𝓻","𝓼","𝓽","𝓾","𝓿","𝔀","𝔁","𝔂","𝔃",
        };

        private string convert(char s) {
            return DATA[s - 'A'];
        }
    }
